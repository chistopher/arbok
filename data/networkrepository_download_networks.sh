download() {
    name=$1
    zip_url=$(curl -s http://networkrepository.com/$name | grep -o "http://nrvis.com/.*zip")
    wget --quiet $zip_url
}

mkdir tmp
cd tmp

i=0
for Network in "${Networks[@]}"; do
    ((i=i+1))
    echo "***********	network $i of ${#Networks[@]}"
    echo "downloading	$Network"
    download $Network
    
    ZIPFile=$(echo *.zip)
    if !(test -f $ZIPFile); then
        echo "No zip-file found for $Network."
        exit 1
    fi

	NAME="${ZIPFile%.*}"

	echo "unzipping	$ZIPFile"
	unzip $ZIPFile > /dev/null
	
	## flatten nested directories and remove directories
	find . -mindepth 2 -type f -exec mv -t . -i '{}' +
	find . -mindepth 1 -type d -exec rmdir '{}' +

    for File in $NAME.mtx $NAME.txt $NAME.edges
    do
        if [[ -f "$File" ]]; then
            echo "found	$File"
            chmod 644 $File
            mv $File ../$TargetDir/
        fi
    done
	
	## clean up unnecessary files
	rm $ZIPFile
	rm -f *.html 2> /dev/null
	rm readme.* 2> /dev/null
	rm $NAME* 2> /dev/null
done

cd ..
rmdir tmp

